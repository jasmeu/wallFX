package eu.jasm.Wall2;

import javafx.scene.image.Image;

public class Frame {

	int x,y,w,h;
	Image i;
	public int getX() {
		return x;
	}
	public void setX(int x) {
		this.x = x;
	}
	public int getY() {
		return y;
	}
	public void setY(int y) {
		this.y = y;
	}
	public int getW() {
		return w;
	}
	public void setW(int w) {
		this.w = w;
	}
	public int getH() {
		return h;
	}
	public void setH(int h) {
		this.h = h;
	}
	public Image getI() {
		return i;
	}
	public void setI(Image i) {
		this.i = i;
	}
	
	
}
